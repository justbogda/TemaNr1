

public class CConfigure{
    int threads_nr;
    int delay;
    String root_director;
    int log_level;

    public CConfigure() {
    }

    public int getThreads_nr() {
        return threads_nr;
    }

    public int getDelay() {
        return delay;
    }

    public String getRoot_director() {
        return root_director;
    }

    public int getLog_level() {
        return log_level;
    }
}
